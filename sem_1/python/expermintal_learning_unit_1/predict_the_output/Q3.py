

# ✅ Return Type of Common Built-in Functions

''''
1. map()
✔ Returns: map object (iterator)

2. filter()
✔ Returns: filter object (iterator)

3. zip()
✔ Returns: zip object (iterator)

4. enumerate()
✔ Returns: enumerate object (iterator)

5. range()
✔ Returns: range object (lazy sequence, iterable but not a list)

6. sorted()
✔ Returns: list

7. reversed()
✔ Returns: reversed object (iterator)

8. iter()
✔ Returns: iterator of the given iterable

9. list()
✔ Returns: list

10. tuple()
✔ Returns: tuple

11. set()
✔ Returns: set

12. dict()
✔ Returns: dictionary

13. all()
✔ Returns: bool
True if all elements are true

14. any()
✔ Returns: bool
True if any one element is true

15. sum()
✔ Returns: int or float

16. max()
✔ Returns: element of the iterable (same data type as elements)

17. min()
✔ Returns: element of the iterable

18. len()
✔ Returns: int

19. type()
✔ Returns: type object
'''